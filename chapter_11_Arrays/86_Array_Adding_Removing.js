let arr = [1, 2, 3];
console.log(arr);
//add one more element at end using push method
arr.push(4);
console.log(arr);

//add multiple elements using push method
arr.push(5, 6);
console.log(arr);

//add element at the begining using unshift method
arr.unshift("Unshift");


//remove element from the end
arr.pop();
console.log(arr);

//remove element from the begining
arr.shift();
console.log(arr);

//add element beging
arr.unshift(200);
console.log(arr);





