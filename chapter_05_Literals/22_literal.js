let age = "Bhaskar";//String literal
let isStudent = true;//Boolean literal
let pi = 3.14;//Numeric literal
let name = "Rao";//String literal
let nullvalue = null;//Null literal
let undefined = undefined;//Undefined literal 

console.log(typeof age); // "string"
console.log(typeof isStudent);  // "boolean"
console.log(typeof pi); // "number"
console.log(typeof name); // "string"
console.log(typeof nullvalue); // "object" (this is a known quirk in JavaScript)
console.log(typeof undefined); // "undefined"